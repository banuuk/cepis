function setNext(args)
{
	if(args == "program"){
		document.add_form.action="add_program_name.cfm";
	} else if( args == "college"){
		document.add_form.action="add_college_name.cfm";	
	} else if( args == "personal"){
		document.add_form.action="add_personal_name.cfm";	
	} else if( args == "computer"){
		document.add_form.action="add_computer_name.cfm";	
	} else if( args == "phone"){
		document.add_form.action="add_phone_name.cfm";	
	} else if( args == "address"){
		document.add_form.action="add_address_name.cfm";	
	}
	document.add_form.prev.value="0";
	document.add_form.next.value="1";
	document.add_form.submit();

}

